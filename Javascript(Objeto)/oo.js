//procedural
processamento(valor1, valor2, valor3);

//oo
objeto = {
    valor1,
    valor2,
    valor3,
    processamento() {
        //...
    }
}

objeto.processamento(); //foco passou a ser o objeto

//principios importantes:
//1. abstração = pegar um objeto do mundo real e traduzir para o sistema de modo que seja mais facil de entender
//2. encapsulamento = ter os detalhes de implementação escondidos e mostrar apenas a interface de comunicação
//3. herança (prototype) = receber atributos e comportamentos de um objeto pai
//4. polimorfismo = multiplas formas, um conceito generico que pode assumir multiplas formas